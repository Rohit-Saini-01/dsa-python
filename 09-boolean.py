var_1 = True  # Boolean values
var_2 = False

num_1 = 10
num_2 = 12

# Relational operators
print(num_1 >= num_2)
print(num_1 <= num_2)
print(num_1 > num_2)
print(num_1 < num_2)
print(num_1 == num_2)
print(num_1 != num_2)

# Logical operators
print(var_1 and var_2)
print(var_1 or var_2)
print(not var_2)
